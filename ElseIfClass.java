
package apnikaksha;

public class ElseIfClass {
    
    public static void main(String[] args){
        
        int n = 23;
        
        if(n <= 0){
            
            System.out.println("n is less than 10");
            
        }else if(n > 10 && n <= 20){
            
            System.out.println("n is greater than 10 and less than 20");
            
        }else if(n > 20 && n <= 30){
            
            System.out.println("n is greater than 20 and less than 30");
        
        }else{
        
        System.out.println("n is greater than 30");
    }
        
    }
    
}
