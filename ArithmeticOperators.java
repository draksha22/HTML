
package apnikaksha;

public class ArithmeticOperators {
    
    public static void main(String[] args){
        
        int a = 23;
        int b = 45;
        
        int c = a + b;
        
        int d = b - a;
        
        int x = b / a;
        
        int y = b % a;
        
        int z = b++;
        
        System.out.println(c);
        System.out.println(d);
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(b);
    }
}
